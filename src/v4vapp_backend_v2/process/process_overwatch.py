class FlowEvent(BaseModel):
    """A recorded event within a flow instance.

    Each event wraps either a LedgerEntry or a TrackedAny operation along
    with identifiers used for matching (group_id, short_id, ledger_type).
    """

    event_type: Literal["ledger", "op"] = Field(..., description="Type of event")
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(tz=timezone.utc),
        description="When the event was recorded",
    )
    ledger_entry: LedgerEntry | None = Field(
        None, description="The ledger entry if event_type='ledger'"
    )
    op: Any = Field(None, description="The TrackedAny operation if event_type='op'")
    group_id: str = Field("", description="Group ID from the event source")
    short_id: str = Field("", description="Short ID from the event source")
    ledger_type: LedgerType | None = Field(None, description="Ledger type if applicable")
    op_type: str | None = Field(None, description="Operation type if applicable")
    group: str = Field(
        "primary",
        description="Which group this event belongs to (primary or a reply group name)",
    )
    event_value: str = Field("", description="Optional value for logging or notifications")

    @classmethod
    def from_ledger_entry(
        cls,
        ledger_entry: LedgerEntry,
        group: str = "primary",
    ) -> FlowEvent:
        """Create a FlowEvent from a LedgerEntry."""
        return cls(
            event_type="ledger",
            timestamp=ledger_entry.timestamp,
            ledger_entry=ledger_entry,
            op=None,
            group_id=ledger_entry.group_id,
            short_id=ledger_entry.short_id,
            ledger_type=ledger_entry.ledger_type,
            op_type=None,
            group=group,
            event_value=ledger_entry.formatted_amount(),
        )

    @classmethod
    def from_op(cls, op: TrackedAny, group: str = "primary") -> FlowEvent:
        """Create a FlowEvent from a TrackedAny operation."""

        if isinstance(op, TrackedForwardEvent):
            event_value = f"{op.amount:,.0f} sats"
        elif op.conv:
            event_value = op.conv.hive_sats_formatted_amount()
        else:
            event_value = ""  # ensure amount_str is populated for logging

        return cls(
            event_type="op",
            timestamp=getattr(op, "timestamp", datetime.now(tz=timezone.utc)),
            ledger_entry=None,
            op=op,
            group_id=getattr(op, "group_id", ""),
            short_id=getattr(op, "short_id", ""),
            ledger_type=None,
            op_type=getattr(op, "op_type", getattr(op, "type", "")),
            group=group,
            event_value=event_value,
        )

    @property
    def log_str(self) -> str:
        """Return a human-readable string summarizing this event for logging."""
        if self.event_type == "ledger" and self.ledger_entry:
            return self.ledger_entry.log_str
        if self.event_type == "op" and self.op:
            try:
                val = getattr(self.op, "log_str", None)
                if val:
                    return val
            except Exception:
                pass
        # Fallback: construct a useful string from the event's own fields
        parts: list[str] = []
        if self.op_type:
            parts.append(self.op_type)
        if self.ledger_type:
            parts.append(self.ledger_type.value)
        if self.short_id:
            parts.append(self.short_id)
        if self.event_value:
            parts.append(self.event_value)
        return " ".join(parts) if parts else "UnknownEvent"


class FlowInstance(BaseModel):
    """A tracked instance of a transaction flow.

    Collects FlowEvents and checks completeness against a FlowDefinition.
    This is the primary object for both real-time tracking and test assertions.
    """

    model_config = ConfigDict(extra="ignore")

    flow_definition: FlowDefinition = Field(
        ..., description="The flow definition this instance tracks"
    )
    trigger_group_id: str = Field("", description="Group ID of the triggering event")
    trigger_short_id: str = Field("", description="Short ID of the triggering event")
    cust_id: str = Field("", description="Customer ID associated with this flow")
    status: FlowStatus = Field(FlowStatus.PENDING, description="Current status of the flow")

    @field_validator("cust_id", mode="before")
    @classmethod
    def _normalize_cust_id(cls, value: Any) -> str:
        if value is None:
            return ""
        return str(value)

    started_at: datetime = Field(
        default_factory=lambda: datetime.now(tz=timezone.utc),
        description="When the flow was initiated",
    )
    completed_at: datetime | None = Field(None, description="When the flow completed")
    superset_grace_expires: datetime | None = Field(
        None,
        description="If this candidate was kept alive as a superset, when the grace period expires",
    )
    superset_winner_name: str | None = Field(
        None,
        description="Name of the sibling flow whose completion triggered the superset grace period",
    )
    events: List[FlowEvent] = Field(
        default_factory=list, description="Recorded events in this flow"
    )
    flow_value: str = Field("", description="Optional value for logging or notifications")
    last_stall_reported_at: datetime | None = Field(
        None, description="When the stall was last logged in the report loop"
    )

    def __init__(
        self,
        flow_definition: FlowDefinition,
        trigger_group_id: str = "",
        trigger_short_id: str = "",
        cust_id: str = "",
        status: FlowStatus = FlowStatus.PENDING,
        started_at: datetime | None = None,
        completed_at: datetime | None = None,
        events: List[FlowEvent] | None = None,
        flow_value: str = "",
        **data: Any,
    ) -> None:
        """Explicit initializer for FlowInstance.

        Pydantic generates a dynamic __init__ that confuses type checkers when
        we instantiate the model with positional/keyword arguments.  Providing
        this signature with defaults makes static analysis happy while still
        delegating to the BaseModel machinery.
        """
        # Deep-copy the definition so each instance has independent stages
        # whose op_log_str can be set without mutating the shared template.
        # When deserialising from Redis (model_validate_json), flow_definition
        # arrives as a plain dict — Pydantic will create a fresh object from
        # it, so no copy is needed.
        if isinstance(flow_definition, FlowDefinition):
            flow_definition = flow_definition.model_copy(deep=True)
        super().__init__(
            flow_definition=flow_definition,
            trigger_group_id=trigger_group_id,
            trigger_short_id=trigger_short_id,
            cust_id=cust_id,
            status=status,
            started_at=started_at or datetime.now(tz=timezone.utc),
            completed_at=completed_at,
            events=events or [],
            flow_value=flow_value,
            **data,
        )

    def add_event(self, event: FlowEvent) -> str | None:
        """Add an event and return the matched stage name, or None if unmatched.

        Only events that match a stage are appended to ``self.events``.
        Unmatched events are silently ignored so they don't pollute the
        event list or interfere with ``_resolve_candidates``.
        """
        # Snapshot which stages are already fulfilled BEFORE checking the event
        # so the new event isn't counted against itself.
        previously_matched = self.matched_stage_names

        # Try to match against stages not yet fulfilled
        for stage in self.flow_definition.stages:
            if stage.name not in previously_matched and stage.matches(event):
                # Event matched — record it
                stage.op_log_str = event.log_str
                self.events.append(event)
                if self.status == FlowStatus.PENDING:
                    self.status = FlowStatus.IN_PROGRESS

                if not self.flow_value and event.event_value:
                    self.flow_value = event.event_value

                if not self.cust_id and event.op and hasattr(event.op, "cust_id"):
                    self.cust_id = getattr(event.op, "cust_id")

                # Check completeness after adding — only record completed_at
                # the first time so late-event absorption doesn't push the
                # timestamp back and fall outside _LATE_EVENT_WINDOW.
                if self.is_complete and self.completed_at is None:
                    self.status = FlowStatus.COMPLETED
                    self.completed_at = event.timestamp
                return stage.name

        return None

    @property
    def matched_stage_names(self) -> set[str]:
        """Return names of stages that have been matched by events.

        Events are replayed **in order** and each event consumes at most
        one unmatched stage — the first one in definition order that
        accepts it.  This mirrors the behaviour of :pymethod:`add_event`
        and prevents a single ``transfer`` event from matching both
        ``trigger_transfer`` and ``change_transfer_op`` when matching is
        group-agnostic.
        """
        matched: set[str] = set()
        for event in self.events:
            for stage in self.flow_definition.stages:
                if stage.name not in matched and stage.matches(event):
                    matched.add(stage.name)
                    break  # this event is consumed — move to the next
        return matched

    @property
    def missing_stages(self) -> List[FlowStage]:
        """Return required stages that have not yet been fulfilled."""
        matched = self.matched_stage_names
        return [s for s in self.flow_definition.required_stages if s.name not in matched]

    @property
    def is_complete(self) -> bool:
        """Check if all required stages have been fulfilled."""
        return len(self.missing_stages) == 0

    @property
    def progress(self) -> str:
        """Human-readable progress summary."""
        total = len(self.flow_definition.required_stages)
        done = total - len(self.missing_stages)
        return f"{done}/{total} required stages complete"

    @property
    def duration(self) -> float | None:
        """Duration in seconds from start to completion, or None if not complete."""
        if self.completed_at:
            return (self.completed_at - self.started_at).total_seconds()
        return None

    @property
    def log_str(self) -> str:
        """Return a human-readable string summarizing this flow instance for logging."""
        name_str = from_snake_case(self.flow_definition.name)

        return (
            f"{name_str} for {self.cust_id} "
            f"{self.flow_value} "
            f"{self.trigger_short_id} "
            f"{self.status} "
            f"{self.progress}"
        )

    @property
    def notification_str(self) -> str:
        """Return a string summarizing this flow instance for notifications."""
        return self.log_str

    @property
    def log_extra(self) -> Dict[str, Any]:
        """
        Return extra fields for logging this flow instance.
        """
        return {"flow": self.summary()}

    def summary(self) -> dict[str, Any]:
        """Return a summary dict of this flow instance."""
        return {
            "flow_type": self.flow_definition.name,
            "trigger_group_id": self.trigger_group_id,
            "trigger_short_id": self.trigger_short_id,
            "cust_id": self.cust_id,
            "status": self.status.value,
            "progress": self.progress,
            "matched_stages": sorted(self.matched_stage_names),
            "missing_stages": [s.name for s in self.missing_stages],
            "event_count": len(self.events),
            "duration": self.duration,
        }


