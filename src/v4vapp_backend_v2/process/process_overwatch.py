class FlowStage(BaseModel):
    """An expected event within a flow definition.

    A stage can represent either a ledger entry (identified by ledger_type)
    or an operation (identified by op_type). Stages are matched against
    incoming FlowEvents to determine flow progress.
    """

    name: str = Field(..., description="Human-readable stage name")
    event_type: Literal["ledger", "op"] = Field(
        ..., description="Whether this stage expects a ledger entry or an operation"
    )
    ledger_type: LedgerType | None = Field(
        None, description="Expected ledger type (for ledger events)"
    )
    op_type: str | None = Field(None, description="Expected operation type (for op events)")
    required: bool = Field(True, description="Whether this stage must be fulfilled")
    group: str = Field(
        "primary",
        description=(
            "Stage group: 'primary' for events sharing the trigger's short_id, "
            "or a descriptive name for reply-linked events"
        ),
    )
    event_filter: Callable[[FlowEvent], bool] | None = Field(
        None,
        description=(
            "Optional callable that receives the FlowEvent and returns True "
            "if the event genuinely matches this stage.  Used to prevent "
            "false-positive matches when the structural criteria (event_type + "
            "op_type/ledger_type) are ambiguous."
        ),
        exclude=True,  # not serialised to Redis — restored from registered definition
    )
    op_log_str: str = Field("", description="The log string for the operation in this flow stage)")

    model_config = ConfigDict(arbitrary_types_allowed=True)

    def __init__(
        self,
        name: str,
        event_type: Literal["ledger", "op"],
        ledger_type: LedgerType | None = None,
        op_type: str | None = None,
        required: bool = True,
        group: str = "primary",
        event_filter: Callable[[FlowEvent], bool] | None = None,
        op_log_str: str = "",
        **data: Any,
    ) -> None:
        """Explicit initializer helps type checkers.

        Without this method Pylance sees the autogenerated pydantic signature and
        complains that ``op_type``/``ledger_type``/``required`` are missing when
        we call ``FlowStage(...)`` with only the relevant fields.  The logic is
        unchanged; we simply delegate to ``BaseModel.__init__``.
        """
        super().__init__(
            name=name,
            event_type=event_type,
            ledger_type=ledger_type,
            op_type=op_type,
            required=required,
            group=group,
            event_filter=event_filter,
            op_log_str=op_log_str,
            **data,
        )

    def matches(self, event: FlowEvent) -> bool:
        """Check if an event fulfils this stage.

        Ledger events are matched solely by ``ledger_type`` (which is unique
        across stages).  Op events are matched by ``op_type`` alone — group
        disambiguation is not performed here because ``db_monitor`` dispatches
        all events with the default group ``"primary"``.  Deduplication in
        :pymethod:`Overwatch._dispatch` prevents re-arrived trigger updates
        from matching the wrong stage.
        """
        if self.event_type != event.event_type:
            return False
        if self.event_type == "ledger":
            matched = self.ledger_type is not None and self.ledger_type == event.ledger_type
        else:
            # op event: match by op_type only (group-agnostic)
            matched = self.op_type is not None and self.op_type == event.op_type
        if matched and self.event_filter is not None:
            try:
                return self.event_filter(event)
            except Exception:
                return False
        return matched


class FlowDefinition(BaseModel):
    """Blueprint describing the expected stages of a complete transaction flow.

    A flow definition is a named template listing every FlowStage that should
    occur for a particular transaction type. FlowInstances are validated
    against a FlowDefinition to determine completeness.
    """

    name: str = Field(..., description="Flow type identifier (e.g. 'hive_to_keepsats')")
    description: str = Field("", description="Human-readable description")
    trigger_op_type: str = Field(..., description="The op_type that initiates this flow")
    stages: List[FlowStage] = Field(
        default_factory=list, description="Ordered list of expected stages"
    )

    @property
    def required_stages(self) -> List[FlowStage]:
        """Return only the stages marked as required."""
        return [s for s in self.stages if s.required]

    @property
    def stage_names(self) -> List[str]:
        """Return names of all stages."""
        return [s.name for s in self.stages]


